package entity;

public class Seeds {
	
	private String start_url;
	private String college;
	private String url_xpath;
	private String word;
	private String title;
	private String speaker;
	private String time;
	private String venue;
	private String text_xpath;
	
	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getSpeaker() {
		return speaker;
	}

	public void setSpeaker(String speaker) {
		this.speaker = speaker;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getVenue() {
		return venue;
	}

	public void setVenue(String venue) {
		this.venue = venue;
	}

	public String getText_xpath() {
		return text_xpath;
	}

	public void setText_xpath(String text_xpath) {
		this.text_xpath = text_xpath;
	}

	public Seeds(){
		
	}

	public String getStart_url() {
		return start_url;
	}

	public void setStart_url(String start_url) {
		this.start_url = start_url;
	}

	public String getCollege() {
		return college;
	}

	public void setCollege(String college) {
		this.college = college;
	}

	public String getUrl_xpath() {
		return url_xpath;
	}

	public void setUrl_xpath(String url_xpath) {
		this.url_xpath = url_xpath;
	}

	public String getWord() {
		return word;
	}

	public void setWord(String word) {
		this.word = word;
	}

	
}
